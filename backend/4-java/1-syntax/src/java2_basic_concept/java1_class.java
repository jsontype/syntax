package java2_basic_concept;

public class java1_class {

	// Class란 무엇인가? : 연관된 변수 + 매소드를 묶어놓고 그것에 이름을 붙인 것.
	public static void main(String[] args) {

		// 예) Math는 클래스가 있고, 그 안에 PI라는 변수, floor, ceil등의 메소드가 있다.
		System.out.println(Math.PI);
		System.out.println(Math.floor(1.6)); // 내림
		System.out.println(Math.ceil(1.6)); // 올림
	}

}